    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        int codigo = Integer.valueOf(txtCodigo.getText());
        String titulo = txtTitulo.getText();
        String letra = txtLetra.getText();
        double tiempoEnMinutos = Integer.valueOf(txtTiempoEnMinutos.getText());
               
        Cancion cancion = new Cancion(codigo, titulo, letra, tiempoEnMinutos);
        cancion.setCodigo(codigo);
        cancion.setTitulo(titulo);
        cancion.setLetra(letra);
        cancion.setTiempoEnMinutos(tiempoEnMinutos);
        
        
        compositor.agregarCancion(codigo, titulo, letra, tiempoEnMinutos);
        JOptionPane.showMessageDialog(this, "La cancion ha sido creada exitosamente! :)");
        this.limpiarCampos();        
        
    }//GEN-LAST:event_btnAceptarActionPerformed

